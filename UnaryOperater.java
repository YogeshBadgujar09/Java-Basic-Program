public class UnaryOperater {
    public static void main(String[] args) {
        int i = 5;
        System.out.println(i); // Output: 5
        System.out.println(i++); // Output: 5
        System.out.println(i); // Output: 6
        System.out.println(--i); // Output: 5
        System.out.println(i); // Output: 5
        System.out.println(i++ + ++i); // Output: 11
        System.out.println(i); // Output: 7
    }
}
